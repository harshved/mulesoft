    //Fetch Data from Database
    public void fetchData(){
        try{
            //1 Load JDBC driver
           Class.forName("com.mysql.jdbc.Driver");

           //2 Define Connnection URL
           String mysqlURL = "jdbc:mysql://localhost:3306/movieslist?user=root";

           //3 Establish Connection
           Connection conn = DriverManager.getConnection(mysqlURL);

           //4 Create a preparedStatement object
           PreparedStatement pst= conn.prepareStatement("SELECT * FROM movie");
           ResultSet rs = pst.executeQuery();
           ResultSetMetaData movieData = rs.getMetaData();
           
           int colCount = movieData.getColumnCount();
           DefaultTableModel recordTable = (DefaultTableModel)jTable1.getModel();
           recordTable.setRowCount(0);
           while(rs.next()){
               Vector colData = new Vector();
               for (int i=1; i<=colCount; i++){
                   colData.add(i);
                   colData.add(rs.getString("movieName"));
                   colData.add(rs.getString("actorName"));
                   colData.add(rs.getString("actressName"));
                   colData.add(rs.getString("directorName"));
                   colData.add(rs.getString("releaseYear"));
               }
               recordTable.addRow(colData);
           }
        } catch(Exception e){ System.out.println(e);}
    }
