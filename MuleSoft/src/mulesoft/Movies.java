    //Insert Function
    void insert(String movieName, String actorName, String actressName, String directorName, String releaseYear) {
        try {
           //1 Load JDBC driver
           Class.forName("com.mysql.jdbc.Driver");

           //2 Define Connnection URL
           String mysqlURL = "jdbc:mysql://localhost:3306/movieslist?user=root";

           //3 Establish Connection
           Connection conn = DriverManager.getConnection(mysqlURL);

           //4 Create a preparedStatement object
           //? shows parameterized query
           String sql = "INSERT INTO movie(movieName, actorName, actressName, directorName, releaseYear) VALUES(?, ?, ?, ?, ?)"; 
           
           PreparedStatement mov = conn.prepareStatement(sql);
           mov.setString(1, movieName);
           mov.setString(2, actorName);
           mov.setString(3, actressName);
           mov.setString(4, directorName);
           mov.setString(5, releaseYear);
           
           //5 Exectue a query
           int rowsInserted = mov.executeUpdate();
           JOptionPane.showMessageDialog(this, "Record inserted : " + rowsInserted);

           //6 Close the connection
           conn.close();
           
           //To reset the values after inserted
           reset();
           
        } catch (ClassNotFoundException ex) {
            System.out.println("Error Loading Driver" + ex);
        } catch (SQLException ex) {
           System.out.println(ex);
        }
    }
